    public void setModel(SinhVien sv){
        textfield_MaSinhVien.setText(sv.getMaSV());
        textfield_HoVaTen.setText(sv.getTenSV());
        if(sv.isGioiTinh()){
            radiobutton_Nam.setSelected(true);
        }else{
            radiobutton_Nu.setSelected(true);
        }
        //load Hinh anh
        lb_HinhAnh.setText("");
        if(sv.getHinhAnh().equalsIgnoreCase("no avatar")){
            lb_HinhAnh.setText("NO AVATAR");
            lb_HinhAnh.setIcon(null);
        }else{
            lb_HinhAnh.setText("");
            ImageIcon imgaIcon = new ImageIcon(getClass().getResource("/images/"+sv.getHinhAnh()));
            Image img = imgaIcon.getImage();
            img.getScaledInstance(lb_HinhAnh.getWidth(), lb_HinhAnh.getHeight(), 0);       
            lb_HinhAnh.setIcon(imgaIcon);
        }
        textfield_NgaySinh.setText(date_form.format(sv.getNgaySinh()));
        textarea_DiaChi.setText(sv.getDiaChi());
    }
