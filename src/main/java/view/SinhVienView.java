    public boolean validateForm(){
        if(Validation.isEmpty(textfield_MaSinhVien, "Ma Sinh Vien khong duoc trong!")){
            return false;
        }
        if(Validation.isEmpty(textfield_HoVaTen, "Ten Sinh Vien khong duoc trong!")){
            return false;
        }
        if(Validation.isEmpty(textfield_NgaySinh, "Ngay sinh khong duoc de trong!")){
            return false;
        }
        if(Validation.isDate(textfield_NgaySinh, "Ngay sinh sai dinh dang")){
            
        }
        if(Validation.isEmpty(textarea_DiaChi, "Dia chi khong duoc de trong!")){
            return false;
        }
        
        return true;
    }
    
    public SinhVien getDuLieu() throws ParseException{
        SinhVien sv = new SinhVien();
        sv.setMaSV(textfield_MaSinhVien.getText());
        sv.setTenSV(textfield_HoVaTen.getText());
        sv.setNgaySinh(date_form.parse(textfield_NgaySinh.getText()));
//        sv.setGioiTinh(rootPaneCheckingEnabled);
        boolean gt = false; // Xet mac dinh gioi tinh ban dau la Nu
        if(radiobutton_Nam.isSelected()){
            gt = true;//Nam
        }
        sv.setGioiTinh(gt);
        sv.setDiaChi(textarea_DiaChi.getText());
        if(strHinhAnh == null){
            sv.setHinhAnh("NO AVATAR");
        }else{
            sv.setHinhAnh(strHinhAnh);
        }
        return sv;
    }
    
    
    private void button_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_SaveActionPerformed
        // TODO add your handling code here:
           if(!validateForm()){
               return;
           }
          try {
             SinhVien sv = getDuLieu();
           if (listSV.getSinhVienByID(sv.getMaSV()) != null) {
               JOptionPane.showMessageDialog(this, "ID of student exist!");
               return;
           }
           else if(listSV.add(sv)>0){
               JOptionPane.showMessageDialog(this, "Luu Thanh Cong");
               fillDataTABLE();
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
       
    }//GEN-LAST:event_button_SaveActionPerformed
